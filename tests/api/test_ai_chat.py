# ============================================
# Mock helpers
# ============================================

def _mock_agent_response(content="Here are the results.", sql=None, docs=None):
    """Create a mock AgentResponse-like object."""
    mock = MagicMock()
    mock.content = content
    mock.sql_generated = sql
    mock.docs_retrieved = docs or []
    return mock


def _mock_agent(session_id="test-session-123"):
    """Create a mock EDSAgent."""
    agent = MagicMock()
    agent.current_session_id = session_id
    agent.chat.return_value = _mock_agent_response()
    agent.start_session.return_value = session_id
    return agent


