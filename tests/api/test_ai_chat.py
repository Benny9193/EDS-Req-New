class TestChatStream:
    """Test POST /api/chat/stream (SSE streaming)."""

    @patch("api.routes.ai_chat.AI_CHAT_ENABLED", True)
    @patch("api.routes.ai_chat._get_or_create_agent")
    def test_stream_returns_sse(self, mock_get_agent, test_client):
        """Streaming endpoint returns SSE chunks followed by done event."""
        agent = _mock_agent()
        agent.chat_stream.return_value = iter(["Hello", " world", "!"])
        mock_get_agent.return_value = (agent, "stream-session-1")

        response = test_client.post("/api/chat/stream", json={
            "message": "Say hello",
        })
        assert response.status_code == 200
        assert "text/event-stream" in response.headers["content-type"]

        # Parse SSE events from response body
        events = []
        for line in response.text.strip().split("\n\n"):
            if line.startswith("data: "):
                events.append(json.loads(line[6:]))

        # Should have 3 chunk events + 1 done event
        assert len(events) == 4
        assert events[0] == {"type": "chunk", "content": "Hello"}
        assert events[1] == {"type": "chunk", "content": " world"}
        assert events[2] == {"type": "chunk", "content": "!"}
        assert events[3]["type"] == "done"
        assert events[3]["session_id"] == "stream-session-1"

    @patch("api.routes.ai_chat.AI_CHAT_ENABLED", False)
    def test_stream_disabled_returns_503(self, test_client):
        """When API key is not configured, streaming returns 503."""
        response = test_client.post("/api/chat/stream", json={
            "message": "hello",
        })
        assert response.status_code == 503
        assert "not available" in response.json()["detail"]

    @patch("api.routes.ai_chat.AI_CHAT_ENABLED", True)
    @patch("api.routes.ai_chat._get_or_create_agent")
    def test_stream_error_event(self, mock_get_agent, test_client):
        """When agent raises mid-stream, an error event is sent."""
        agent = _mock_agent()
        def _exploding_stream(**kwargs):
            yield "partial"
            raise RuntimeError("LLM crashed")
        agent.chat_stream.side_effect = _exploding_stream
        mock_get_agent.return_value = (agent, "stream-session-err")

        response = test_client.post("/api/chat/stream", json={
            "message": "Will fail mid-stream",
        })
        assert response.status_code == 200

        events = []
        for line in response.text.strip().split("\n\n"):
            if line.startswith("data: "):
                events.append(json.loads(line[6:]))

        # Should have 1 chunk + 1 error
        assert len(events) == 2
        assert events[0] == {"type": "chunk", "content": "partial"}
        assert events[1]["type"] == "error"
        # Error messages are user-friendly (not raw exception text)
        assert "message" in events[1]
        assert len(events[1]["message"]) > 0

    @patch("api.routes.ai_chat.AI_CHAT_ENABLED", True)
    @patch("api.routes.ai_chat._get_or_create_agent")
    def test_stream_connection_error_message(self, mock_get_agent, test_client):
        """Connection/timeout errors get a user-friendly message."""
        agent = _mock_agent()
        def _timeout_stream(**kwargs):
            raise ConnectionError("connection timeout after 30s")
        agent.chat_stream.side_effect = _timeout_stream
        mock_get_agent.return_value = (agent, "stream-session-conn")

        response = test_client.post("/api/chat/stream", json={
            "message": "Will timeout",
        })
        events = []
        for line in response.text.strip().split("\n\n"):
            if line.startswith("data: "):
                events.append(json.loads(line[6:]))

        assert len(events) == 1
        assert events[0]["type"] == "error"
        assert "connecting" in events[0]["message"].lower() or "trouble" in events[0]["message"].lower()

    @patch("api.routes.ai_chat.AI_CHAT_ENABLED", True)
    @patch("api.routes.ai_chat._get_or_create_agent")
    def test_stream_status_events(self, mock_get_agent, test_client):
        """Status markers from agent are converted to status events."""
        agent = _mock_agent()
        agent.chat_stream.return_value = iter([
            "[STATUS:Searching catalog...]",
            "[STATUS:]",
            "Here are the results.",
        ])
        mock_get_agent.return_value = (agent, "stream-session-status")

        response = test_client.post("/api/chat/stream", json={
            "message": "Find pencils",
        })
        events = []
        for line in response.text.strip().split("\n\n"):
            if line.startswith("data: "):
                events.append(json.loads(line[6:]))

        # Should have: status, status-clear, chunk, done
        assert len(events) == 4
        assert events[0] == {"type": "status", "content": "Searching catalog..."}
        assert events[1] == {"type": "status", "content": ""}
        assert events[2] == {"type": "chunk", "content": "Here are the results."}
        assert events[3]["type"] == "done"

    def test_stream_empty_message_422(self, test_client):
        """Empty message on streaming endpoint returns 422."""
        response = test_client.post("/api/chat/stream", json={
            "message": "",
        })
        assert response.status_code == 422


